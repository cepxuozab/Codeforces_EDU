#include <iostream>
#include <vector>
#include <string>
#include <algorithm>

// Функция для вычисления Z-функции строки s
// z[i] — длина наибольшего общего префикса строки s и суффикса s, начинающегося в i
auto compute_z_function(std::string const& s) -> std::vector<int>
{
    int n = static_cast<int>(s.length());
    std::vector<int> z(n);
    for (int i = 1, l = 0, r = 0; i < n; ++i)
    {
        if (i <= r)
        {
            z[i] = std::min(r - i + 1, z[i - l]);
        }
        while (i + z[i] < n && s[z[i]] == s[i + z[i]])
        {
            ++z[i];
        }
        if (i + z[i] - 1 > r)
        {
            l = i;
            r = i + z[i] - 1;
        }
    }
    return z;
}

auto main() -> int
{
    // Оптимизация ввода-вывода
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    std::string s;
    if (std::cin >> s)
    {
        int n = static_cast<int>(s.length());

        // Создаём строку t = s + s[0..n-2] для работы с циклическими суффиксами
        // Длина t = 2n - 1, этого достаточно для сравнения любых двух циклических суффиксов
        std::string t = s;
        t += s.substr(0, n - 1);

        // Вычисляем Z-функцию для строки t
        // z[i] показывает длину наибольшего общего префикса между t[0..] и t[i..]
        std::vector<int> z = compute_z_function(t);

        // Считаем, сколько циклических суффиксов лексикографически меньше суффикса 0
        // Суффикс 0 — это наша исходная циклическая строка s~
        int count_smaller = 0;

        for (int i = 1; i < n; ++i)
        {
            // Сравниваем циклический суффикс, начинающийся в позиции 0,
            // с циклическим суффиксом, начинающимся в позиции i

            // z[i] показывает длину общего префикса между этими двумя суффиксами
            int lcp = z[i];

            if (lcp >= n)
            {
                // Суффиксы равны на всей длине n
                // По условию, если суффиксы равны, меньший индекс идёт первым
                // Так как 0 < i, суффикс 0 идёт перед суффиксом i
                // Значит суффикс i НЕ меньше суффикса 0
            }
            else
            {
                // Суффиксы различаются на позиции lcp
                // Сравниваем символы t[lcp] (из суффикса 0) и t[i + lcp] (из суффикса i)
                if (t[i + lcp] < t[lcp])
                {
                    // Суффикс i лексикографически меньше суффикса 0
                    ++count_smaller;
                }
                // Иначе суффикс 0 меньше суффикса i
            }
        }

        // Позиция суффикса 0 в отсортированном списке (1-индексированная)
        // = количество суффиксов, которые меньше него + 1
        std::cout << count_smaller + 1 << "\n";
    }
    return 0;
}
