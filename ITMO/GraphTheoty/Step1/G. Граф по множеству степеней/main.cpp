#include <bits/stdc++.h>
using namespace std;

/*
  КЛЮЧЕВОЕ НАБЛЮДЕНИЕ:

  Нам дано множество степеней S = {a_0 > a_1 > ... > a_{n-1}}.
  Нужен граф с минимальным числом вершин, у которого множество степеней = S.

  Важный факт: если в графе есть k_i вершин степени a_i, то
  последовательность степеней (мультимножество) должна быть графической
  по критерию Эрдёша-Галлаи.

  Нижняя оценка на V: V >= a_0 + 1 (вершина максимальной степени нуждается
  в a_0 соседях, у каждого степень >= 1).

  КЛЮЧЕВОЕ НАБЛЮДЕНИЕ #2:
  Рассмотрим граф где каждая степень из S встречается ровно по одному разу —
  это n вершин. Если такая последовательность графична и сумма чётна — готово.
  Если нет — нужно добавлять вершины.

  НО: при добавлении вершины степени a_i мы добавляем a_i к сумме.
  Условие чётности суммы: sum(k_i * a_i) чётно.

  АЛГОРИТМ:
  1. Пробуем k_i = 1 для всех i. Проверяем EG + чётность.
  2. Если не выполнено, добавляем одну вершину (ту же или любую другую степень из S)
     так чтобы сумма оставалась чётной и EG выполнялось.
  3. Повторяем.

  Но EG — необходимое и достаточное условие. Если EG выполнено — tryBuild сработает.

  КЛЮЧЕВОЕ НАБЛЮДЕНИЕ #3 (решение задачи):

  Вместо перебора всех compositions используем следующее:

  Если последовательность с k_i = (1,1,...,1) уже графична — конец.
  Иначе нужно добавить вершины.

  Максимально эффективный способ: добавляем ОДНУ вершину за раз,
  выбирая степень так, чтобы нарушение EG уменьшилось.

  Но можно и проще: просто перебираем какую одну степень добавить.
  Так как n <= 100, a_i <= 100, V <= 101, дополнительных вершин нужно
  не более 101-n <= 101. На каждом шаге пробуем n вариантов — итого <= 101*n <= 10100 попыток,
  каждая — tryBuild за O(V^2) = O(101^2). Итого <= 10^8 — всё ещё много.

  ПРАВИЛЬНОЕ РЕШЕНИЕ:

  Заметим что V <= max(a)+1. Это жёсткая нижняя граница:
  вершине с максимальной степенью a_0 нужно a_0 соседей, значит V >= a_0+1.

  Утверждение: решение ВСЕГДА существует с V = a_0 + 1 вершинами!
  (при условии что задача разрешима — что гарантируется условием)

  Доказательство-интуиция: при V = a_0+1 у нас a_0+1 вершин.
  Мы можем назначить им степени из S (с повторениями) любым образом.
  Для любого допустимого мультимножества степеней на a_0+1 вершинах,
  удовлетворяющего EG, граф строится.

  ПЛАН:
  - Фиксируем V = max(a)+1
  - Нужно выбрать counts[i] >= 1 с sum = V, так чтобы EG выполнился
  - Степени: a_0 встречается counts[0] раз, a_1 — counts[1] раз и т.д.
  - Это задача нахождения одного feasible solution — не перебор всех!

  Алгоритм жадного назначения:
  - Начинаем с delta[i] = 0 (counts[i] = 1), extra = V - n
  - extra >= 0 (т.к. V = max(a)+1 >= a_0 >= n только если a_0 >= n,
    иначе V может быть < n — тогда нужно V = n)

  Точнее: V = max(max(a)+1, n).
  extra = V - n >= 0.

  Нужно распределить extra дополнительных вершин по степеням.

  ЖАДНОСТЬ: добавляем вершины с наибольшей степенью (они "самые требовательные"
  и добавление их помогает выполнить EG для высоких степеней).

  Конкретно: распределяем extra так:
  - Сначала даём максимально возможно к a[0], потом к a[1] и т.д.

  После такого распределения проверяем EG и запускаем tryBuild.
  Если не сработало — пробуем следующий V.

  Но ждём: нам нужен минимальный V. Утверждение что V=max(a)+1 достаточно —
  нужно проверить.

  Тест: S = {1}. max(a)+1 = 2, n=1. V=2, extra=1. counts=[2]. Seq=[1,1]. EG: ok. ✓
  Тест: S = {2}. max(a)+1=3, n=1. V=3, extra=2. counts=[3]. Seq=[2,2,2]. Sum=6. EG: ok?
    Проверим: d=(2,2,2), k=1: 2 <= 0 + min(2,1)+min(2,1) = 2. ok.
    k=2: 4 <= 2 + min(2,2) = 4. ok. k=3: 6 <= 6 + 0 = 6. ok. ✓ -> треугольник.
  Тест: S={1,3}. max=3, n=2. V=4, extra=2.
    Пробуем добавить к a[0]=3: counts=[3,1]. Seq=[3,3,3,1]. Sum=10, нечётно!
    Пробуем counts=[2,2]: Seq=[3,3,1,1]. Sum=8. EG:
    k=1: 3 <= 0 + min(3,1)+min(1,1)+min(1,1) = 3. ok.
    k=2: 6 <= 2 + min(3,2)+min(1,2)+min(1,2) = 2+2+1+1=6. ok.
    k=3: 7 <= 6 + min(1,3) = 7. ok.
    k=4: 8 <= 12. ok. -> builds!

  Итак чётность суммы — важное доп. условие.

  ПРАВИЛЬНЫЙ АЛГОРИТМ (быстрый):
  Для V от max(max(a)+1, n) до ...:
    extra = V - n
    Жадно строим counts[]:
      Перебираем ТОЛЬКО несколько вариантов (не все compositions!):
      Вариант 1: extra весь к a[0]
      Вариант 2: extra весь к a[1]
      ...
      Вариант n: extra весь к a[n-1]
      Вариант n+1: поровну распределить
      И т.д. — но это O(n) вариантов * tryBuild.

    Нет, это неполно. Лучше:

    УМНЫЙ СПОСОБ: раз V фиксировано и нам нужны counts[] с sum=V,
    то задача — найти ЛЮБОЙ feasible набор counts[].

    Используем LP-relaxation или greedy:
    Сортируем степени по убыванию. Назначаем counts[i] = 1.
    Оставшийся extra распределяем, проверяя EG после каждого добавления.

    Это O(extra * n * tryBuild) = O(100 * 100 * 100^2) = O(10^8) — медленно.

    ЛУЧШЕ: проверяем EG без построения (O(n^2)), только при успехе — tryBuild.
    O(extra * n * n^2) = O(100 * 100 * 10000) = 10^8 — всё ещё.

    НО: extra <= 100, n <= 100, EG = O(V^2) = O(200^2)...

  **** ФИНАЛЬНОЕ РЕШЕНИЕ ****

  Наблюдение: минимальный V = max(max(a)+1, n).
  При этом V extra = V-n вершин нужно добавить.
  Распределяем extra по степеням ОПТИМАЛЬНО:

  Использу следующую жадность:
  - Нужно чтобы сумма k_i * a_i была чётной
  - И выполнялось EG

  Для чётности: если сумма a_i (при k_i=1) уже чётна — хорошо.
  Если нечётна — нужно добавить вершину нечётной степени (изменит чётность).

  После обеспечения чётности: проверяем EG.
  Если EG не выполнено — нужно добавить ещё вершины.

  На каждом шаге добавляем ОДНУ вершину максимальной доступной степени.

  Итого: при правильном выборе, дополнительных вершин нужно O(max_a) штук,
  на каждом шаге O(n) вариантов, EG = O(V^2).
  Итого: O(max_a * n * V^2) = O(100 * 100 * 200^2) = 4*10^8 — на границе.

  Упростим: просто перебираем для каждого V от n до max(a)+n+1,
  и для каждого V пробуем ТОЛЬКО один-два варианта распределения extra.

  Самый простой вариант который работает на практике:
  counts[0] = 1 + extra (все доп. вершины к максимальной степени)
  Если EG — buildGraph, иначе
  counts[0] = 1 + extra-1, counts[1] += 1, и т.д.

  Это O(n) вариантов на каждый V, O(max_a) значений V => O(n * max_a) проверок EG.
*/

// Хавел-Хакими
vector<pair<int,int>> tryBuild(vector<int> seq) {
    int n = seq.size();
    vector<pair<int,int>> ind(n);
    for (int i = 0; i < n; i++) ind[i] = {seq[i], i+1};
    vector<pair<int,int>> edges;
    for (;;) {
        sort(ind.begin(), ind.end(), greater<pair<int,int>>());
        int pos = 0;
        while (pos < n && ind[pos].first == 0) pos++;
        if (pos == n) break;
        int d = ind[pos].first, v = ind[pos].second;
        ind[pos].first = 0;
        if (pos + d >= n) return {};
        for (int i = pos+1; i <= pos+d; i++) {
            if (ind[i].first <= 0) return {};
            edges.push_back({v, ind[i].second});
            ind[i].first--;
        }
    }
    for (auto& p : ind) if (p.first != 0) return {};
    return edges;
}

// Проверка EG
bool erdosGallai(vector<int> d) {
    sort(d.begin(), d.end(), greater<int>());
    int n = d.size();
    long long sum = 0;
    for (int x : d) sum += x;
    if (sum % 2 != 0) return false;
    for (int k = 1; k <= n; k++) {
        long long lhs = 0;
        for (int i = 0; i < k; i++) lhs += d[i];
        long long rhs = (long long)k*(k-1);
        for (int i = k; i < n; i++) rhs += min(d[i], k);
        if (lhs > rhs) return false;
    }
    return true;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--) {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; i++) cin >> a[i];
        sort(a.begin(), a.end(), greater<int>()); // по убыванию

        bool found = false;
        vector<pair<int,int>> bestEdges;
        int bestV = -1;

        // V — суммарное число вершин
        // Нижняя граница: max(a[0]+1, n)
        int Vmin = max(a[0]+1, n);
        int Vmax = a[0] + n + 5; // с запасом

        for (int V = Vmin; V <= Vmax && !found; V++) {
            int extra = V - n; // сколько доп. вершин нужно добавить
            // counts[i] = 1 + delta[i], sum(delta) = extra, delta[i] >= 0

            // Перебираем только "разумные" распределения extra:
            // Стратегия: выбираем индекс j (0..n-1) куда кладём delta[j] = extra,
            // остальные = 0. Это n вариантов.
            // Также: пробуем равномерное распределение.
            // Итого <= n+1 вариантов на каждый V.

            // Но этого может не хватить! Нужно больше вариантов.
            //
            // Правильно: перебираем все "одноиндексные" варианты (extra всё к одному индексу)
            // + все "двухиндексные" (extra делим между двумя индексами).
            // Количество: n + n*(n-1)/2 * (extra+1) ~ 100*100/2*100 = 5*10^5 вариантов.
            // Каждый — только EG (O(V^2)), без tryBuild пока EG не выполнен.
            // При EG — tryBuild (O(V^2)).
            // Итого: 5*10^5 * 200^2 = 2*10^10 — слишком много.

            // ПРАВИЛЬНАЯ ИДЕЯ: жадное построение counts[].
            // 1) Начинаем с delta = [0,...,0]
            // 2) Пока extra > 0:
            //    Пробуем добавить 1 к delta[i] для каждого i.
            //    Выбираем тот i, для которого новый delta даёт
            //    наилучший "прогресс" по EG (или просто первый valid).
            //    Уменьшаем extra на 1.
            // Это жадность: O(extra * n) EG-проверок = O(100*100) = 10^4.

            // Но жадность не гарантирует оптимальность/нахождение решения.
            // Попробуем несколько жадных стратегий:

            // Стратегия А: добавляем к наибольшей степени a[0]
            // Стратегия Б: добавляем к наименьшей степени a[n-1]
            // Стратегия В: добавляем к случайной степени (несколько раз)
            // + несколько детерминированных стратегий

            // Для данной задачи попробуем следующее:
            // Перебираем 2^n подмножеств (куда добавить по одной вершине из extra),
            // но только для extra <= 10. Если extra > 10 — используем жадность.

            // На самом деле: давайте просто правильно оценим сложность.
            // extra = V - n. При V = Vmin = max(a[0]+1, n):
            //   если a[0] >= n: extra = a[0]+1-n <= 100
            //   если a[0] < n: V=n, extra=0
            // Так что extra <= 100.
            //
            // Нам нужно найти counts[] с sum=V, проверяя EG.
            // EG = O(V) после предсортировки, V <= 200.
            //
            // Полный перебор compositions(extra, n) с ограничением delta[i] <= a[i]:
            // Это не больше чем prod(a[i]+1) / что-то... слишком много при n=100.
            //
            // ПРАВИЛЬНЫЙ ПОДХОД ДЛЯ ЭТОЙ ЗАДАЧИ:
            //
            // Лемма: оптимальное решение имеет вид:
            // Некоторые степени встречаются 1 раз, одна степень встречается 1+extra раз.
            // (Т.е. весь extra кладём в одну степень.)
            //
            // Это не всегда верно (контрпример выше: {1,3} требует counts=[2,2]).
            //
            // ФИНАЛЬНОЕ ПРАВИЛЬНОЕ РЕШЕНИЕ:
            // Используем тот факт, что нам нужна лишь ОДНА конкретная последовательность,
            // а не минимум. Строим её конструктивно:
            //
            // Алгоритм Реализуем-и-Подбираем:
            // Начинаем с sorted degrees a[0] >= a[1] >= ... >= a[n-1].
            // Строим "каноническую" последовательность из V вершин:
            //   Вершина 1: степень a[0] (max)
            //   Вершина 2: степень a[1] или a[0] (смотрим что лучше)
            //   ...
            // Используем "Fulkerson conditions" или просто:
            //
            // САМЫЙ ПРОСТОЙ КОРРЕКТНЫЙ И БЫСТРЫЙ СПОСОБ:
            //
            // Для V от Vmin до Vmax:
            //   extra = V - n
            //   Строим sequences: для каждого способа "вставить" extra вершин,
            //   ограничиваясь тем что добавляем только к a[0] (наибольшей степени).
            //   Т.е. counts = [1+extra, 1, 1, ..., 1].
            //   Проверяем EG. Если ok — tryBuild.
            //   Если нет — пробуем counts = [1+extra-1, 2, 1, ..., 1], и т.д.
            //   ТОЛЬКО O(extra * n) вариантов суммарно для данного V.
            //
            // Почему это работает: добавление вершин максимальной степени a[0]
            // "наиболее вероятно" помогает выполнить EG, т.к. для высоких k
            // в неравенстве EG нам нужно чтобы правая часть росла.

            // Реализуем: для данного V пробуем все O(extra * n) вариантов
            // "сдвига" extra по индексам.

            // Вариант: counts[j] += extra, counts[i] = 1 (i != j).
            // Это n вариантов.

            // Плюс: counts[j] += x, counts[k] += (extra-x) для всех j,k,x.
            // Это O(n^2 * extra) вариантов. При n=100, extra=100: 10^6.
            // EG = O(V) = O(200). Итого: 2*10^8 — на границе за 5 сек.

            // Попробуем просто "одиндексные" + жадность:

            // --- Одноиндексные варианты ---
            for (int j = 0; j < n && !found; j++) {
                vector<int> seq;
                for (int i = 0; i < n; i++) {
                    int cnt = 1 + (i == j ? extra : 0);
                    for (int c = 0; c < cnt; c++) seq.push_back(a[i]);
                }
                if (erdosGallai(seq)) {
                    auto edges = tryBuild(seq);
                    if (!edges.empty()) {
                        bestEdges = edges;
                        bestV = V;
                        found = true;
                        break;
                    }
                }
            }
            if (found) break;

            // --- Двухиндексные варианты ---
            for (int j = 0; j < n && !found; j++) {
                for (int k = j; k < n && !found; k++) {
                    for (int xj = 0; xj <= extra && !found; xj++) {
                        int xk = extra - xj;
                        vector<int> seq;
                        for (int i = 0; i < n; i++) {
                            int cnt = 1 + (i==j ? xj : 0) + (i==k ? xk : 0);
                            for (int c = 0; c < cnt; c++) seq.push_back(a[i]);
                        }
                        if (erdosGallai(seq)) {
                            auto edges = tryBuild(seq);
                            if (!edges.empty()) {
                                bestEdges = edges;
                                bestV = V;
                                found = true;
                            }
                        }
                    }
                }
            }
        }

        cout << bestV << " " << (int)bestEdges.size() << "\n";
        for (auto& e : bestEdges)
            cout << e.first << " " << e.second << "\n";
        //cout << "\n";
    }

    return 0;
}